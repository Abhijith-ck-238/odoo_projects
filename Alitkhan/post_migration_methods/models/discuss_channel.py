    def _copy_message_to_discuss_channel(self, message, channel):
        """Copy a chatter message to a Discuss channel without moving it."""
        if not message or not channel:
            return False
        if message.model == 'discuss.channel' and message.res_id == channel.id:
            return message

        existing_copy = self.env['mail.message'].sudo().search([
            ('model', '=', 'discuss.channel'),
            ('res_id', '=', channel.id),
            ('date', '=', message.date),
            ('author_id', '=', message.author_id.id),
            ('message_type', '=', message.message_type),
            ('body', '=', message.body or ''),
        ], limit=1)
        if existing_copy:
            return existing_copy

        return message.sudo().copy({
            'model': 'discuss.channel',
            'res_id': channel.id,
            'record_name': channel.display_name,
            'message_id': False,
        })

    def _get_original_thread_from_message_id(self, message):
        """Extract the original thread from migrated Odoo message-id values.

        Expected format example:
        <...-openerp-34948-contract.contract@alitkan>
        """
        if not message.message_id:
            return False, False
        match = re.search(r'-openerp-(\d+)-([a-zA-Z0-9_.]+)@', message.message_id)
        if not match:
            return False, False
        res_id = int(match.group(1))
        model_name = match.group(2)
        if not self.env.registry.get(model_name):
            return False, False
        record = self.env[model_name].sudo().browse(res_id).exists()
        if not record:
            return False, False
        return model_name, record

    def restore_chatter_messages_moved_to_discuss(self):
        """Recreate chatter copies for messages previously moved to channels.

        The old action changed mail.message.model/res_id to discuss.channel.
        Chatter only loads messages whose model/res_id point to the business
        document, so those rows disappeared from their original records. This
        method leaves the channel rows in place and creates a second message on
        the original record when it can infer the original thread from
        message_id.
        """
        moved_messages = self.env['mail.message'].sudo().search([
            ('model', '=', 'discuss.channel'),
            ('message_id', 'ilike', '-openerp-'),
        ], order='date asc')

        restored_count = 0
        skipped_count = 0
        for message in moved_messages:
            model_name, record = self._get_original_thread_from_message_id(message)
            if not record:
                skipped_count += 1
                continue

            existing_message = self.env['mail.message'].sudo().search([
                ('model', '=', model_name),
                ('res_id', '=', record.id),
                ('date', '=', message.date),
                ('author_id', '=', message.author_id.id),
                ('message_type', '=', message.message_type),
                ('body', '=', message.body or ''),
            ], limit=1)
            if existing_message:
                skipped_count += 1
                continue

            message.sudo().copy({
                'model': model_name,
                'res_id': record.id,
                'record_name': record.display_name,
                'message_id': False,
            })
            restored_count += 1

        _logger.info(
            "Restored %s chatter messages moved to discuss channels; skipped %s.",
            restored_count, skipped_count,
        )
        return restored_count

    def fix_old_discuss_messages(self):
        """Fix old messages that lack 'Message posted from' prefix in Contracts/KS-OR channel"""
        # Find the channel 'Contracts/KS-OR' (id=1635)
        channel = self.search([('id', '=', 1635)])
        if not channel:
            print("Channel 'Contracts/KS-OR' (ID: 1635) not found")
            return
        
        print(f"Processing channel: {channel.name} (ID: {channel.id})")
        
        # Define the cutoff date: 15/01/2026 12:00AM
        cutoff_date = datetime(2026, 1, 15, 0, 0, 0)
        
        # Find messages in this channel before the cutoff date
        messages = self.env['mail.message'].sudo().search([
            ('res_id', '=', channel.id),
            ('model', '=', 'discuss.channel'),
            ('date', '<', cutoff_date),
            ('message_type', '=', 'comment'),
            ('body', '!=', False),
        ], order='date asc')
        
        print(f"Found {len(messages)} messages before {cutoff_date}")
        
        updated_count = 0
        skipped_count = 0
        
        for message in messages:
            # Check if message already has the prefix
            if 'Message posted from' in message.body:
                skipped_count += 1
                continue
            
            # Check if message has record_name field
            if not message.record_name:
                skipped_count += 1
                continue
            
            # Extract contract ID from message_id field
            # Example: <535781299354691.1765786634.085067749023438-openerp-34948-contract.contract@alitkan>
            # In this format, 34948 is the contract ID
            related_record = None
            related_model = None
            
            if message.message_id:
                try:
                    # Extract the ID from the message_id
                    # Pattern: ...-openerp-{ID}-contract.contract@...
                    import re
                    match = re.search(r'-openerp-(\d+)-contract\.contract@', message.message_id)
                    if match:
                        contract_id = int(match.group(1))
                        contract_record = self.env['contract.contract'].sudo().browse(contract_id)
                        if contract_record.exists():
                            related_record = contract_record
                            related_model = 'contract.contract'
                            print(f"Found contract: {contract_record.name} (ID: {contract_id})")
                        else:
                            print(f"Contract ID {contract_id} not found")
                    else:
                        print(f"Could not extract contract ID from message_id: {message.message_id}")
                except Exception as e:
                    print(f"Error parsing message_id: {e}")
            
            if not related_record:
                skipped_count += 1
                print(f"Could not extract contract ID from message_id: {message.message_id}")
                continue
            
            # Create the prefix
            model_description = self.env['ir.model']._get(related_model).display_name or related_model
            original_url = f"/odoo/{related_model}/{related_record.id}"
            contract_name = related_record.name
            
            prefix = f'<p><em>Message posted from <a href="{original_url}">{model_description}: {contract_name}</a></em></p>'
            new_body = Markup(prefix) + (Markup(message.body) if not isinstance(message.body, Markup) else message.body)
            
            # Update the message
            message.sudo().write({'body': str(new_body)})
            updated_count += 1
            print(f"Updated message from {message.date}: '{contract_name}' -> {related_model}")
        
        print(f"Update complete: {updated_count} messages updated, {skipped_count} messages skipped")
        return updated_count
